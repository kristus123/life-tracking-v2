function plus_percentage(value, percentage) {
    return value * (1.0 + (percentage / 100.0))
}

function percentage_increase(originalNumber, newNumber) {
	return ((newNumber - originalNumber) / originalNumber) * 100
}

function preparedGramsToRawGrams(rawWeightInGram, averagePreparedWeight, preparedGramsEaten) {
	const increase = percentage_increase(averagePreparedWeight, rawWeightInGram)
	return plus_percentage(preparedGramsEaten, increase)
}

